public class loop20 {
    public static void main(String[] args) {
      //  int count=0;
        for(int i=5;i>=1;i--)
        {
            for(int j =1;j<=i;j++)
            {
             //   ++count;
                System.out.print(j+" ");
            }
            System.out.println("");
        }
    }
}
